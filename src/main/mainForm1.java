    private void txt_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_searchKeyReleased
        // TODO add your handling code here:
        try {

            String sql = "SELECT * FROM `employee_list` WHERE `srno`=?";

            ps = conn.prepareStatement(sql);
            ps.setString(1, txt_search.getText());
            st = ps.executeQuery();

            if (st.next()) {
                txtFname5.setText(st.getString(2));
                txtLname5.setText(st.getString(3));
                txtDepartment6.setText(st.getString(4));
                txtDesignation6.setText(st.getString(5));
                txtNumber5.setText(st.getString(6));
                if (st.getString(7).equals("Active")) {
                    r_Active5.setSelected(true);
                    r_Inactive5.setSelected(false);
                } else if (st.getString(7).equals("In - Active")) {
                    r_Active5.setSelected(false);
                    r_Inactive5.setSelected(true);
                }
                txtAddress5.setText(st.getString(8));
                txt_salary1.setText(st.getString(9));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {

            try {

                st.close();
                ps.close();

            } catch (Exception e) {

            }
        }
    }//GEN-LAST:event_txt_searchKeyReleased

